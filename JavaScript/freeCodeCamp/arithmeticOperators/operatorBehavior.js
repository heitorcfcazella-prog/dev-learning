/*
Podemos efetuar a atribuição composta, onde podemos efetuar uma
operação matemática e reatribuir o valor para a variável em
uma linha de código
*/

let num = 5;
num = num + 2;

console.log(num);

// ou

num = 5;
num += 2; //incrementa

console.log(num);

//+= adiciona um valor em uma variável já existente

let total = 10;
total += 5;

console.log(total);

//-= remove um valor em uma variável já existente

let score = 20;
score -= 7;

console.log(score);

// *= multiplica o valor atual pelo número especificado

let points = 5;
points *= 3;

console.log(points);

// /= divide o valor atual pelo número especificado

let balance = 100;
balance /= 4;

console.log(balance);

