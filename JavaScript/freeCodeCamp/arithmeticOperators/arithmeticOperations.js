// Soma(+)

const num1 = 10;
const num2 = 5;
const num3 = 15;

const result1 = num1 + num2;
const result2 = num2 + num3;
const result3 = num1 + num2 + num3;

console.log(result1); // 15
console.log(result2); // 20
console.log(result3); // 30

//Subtraçã(-)

const difference1 = num1 - num2;
const difference2 = num2 - num1;
console.log(difference1); // 5
console.log(difference2); // -5

//Multiplicação (*)

const multResult1 = num1 * num2;
const multResult2 = num2 * num3;
const multResult3 = num1 * num2 * num3;

console.log(multResult1); // 50
console.log(multResult2); // 75 
console.log(multResult3); // 750

// Divisão(/)

const divResult1 = num1 / num2;
const divResult2 = num2 / num1;
const divResult3 = num2 / num1 / num3;

console.log(divResult1);
console.log(divResult2);
console.log(divResult3);

//Infinidade

const infinite = 10 / 0;
console.log(infinite);

//Resto (%)

const resto = 10 % 3;
console.log(resto);

//Exponenciação (**)

const exponent = num1 ** num2;
console.log(exponent); //100000