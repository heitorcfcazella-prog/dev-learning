/*
Operadores de comparação permitem comparar dois valores e 
retornar true ou false. Ele usado para sistemas com if, 
loops, etc
*/

/* > é para comparar se o valor da esquerda é maior do que
o da direita */

let a = 6;
let b = 9; 

console.log(a > b);
console.log(b > a);

/* >= é para comparar se o valor da esquerda é maior ou
igual ao da direita */

let c = 6;

console.log(a >= b);
console.log(b >= a);
console.log(a >= c);

/* < é para comparar se o valor da esquerda é menor
do que o da direita */

console.log(a < b);
console.log(b < a);

/* <= é para comparar se o valor da esquerda é menor ou
igual ao da direita */

console.log(a <= b);
console.log(b <= a);
console.log(a <= c);
